package com.uce.edu.demo.consultorio;

public class Paciente {
	
	
	private String nombre;
	private String edad;
	
	//GET Y SET
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getEdad() {
		return edad;
	}
	public void setEdad(String edad) {
		this.edad = edad;
	}
	
	

}
